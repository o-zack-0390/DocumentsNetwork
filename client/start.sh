#!/bin/bash
# start.sh

# http-serverを起動（バックグラウンドで）
http-server  -p 3000 &
SERVER_PID=$!

# サーバーが起動するまで少し待機
sleep 1

# カスタムメッセージを表示
echo "================================================================="
echo "アプリケーションが起動しました！"
echo "使用するには以下のURLを開いてください："
echo "http://localhost:3000/"
echo "================================================================="

# 元のプロセスが終了するまで待機
wait $SERVER_PID